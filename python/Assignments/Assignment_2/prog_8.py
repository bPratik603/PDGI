intVal = 10;
floatVal = 5.5;
stringVal = "Python";


print("Original Values");
print("intVal = ",intVal," Type: ",type(intVal));
print("floatVal = ",floatVal," Type: ",type(floatVal));
print("stringVal = ",stringVal," Type: ",type(stringVal));

print('\n');

print("intVal + floatVal = ",intVal + floatVal," Type: ",type(intVal + floatVal));

print("intVal * floatVal = ",intVal * floatVal," Type: ",type(intVal * floatVal));

print("floatVal / intVal = ",floatVal / intVal," Type: ",type(floatVal / intVal));

print("stringVal * 2 = ",stringVal * 2," Type: ",type(stringVal * 2));